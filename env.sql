-- Создание схемы
CREATE SCHEMA IF NOT EXISTS psd;

-- Отключаем зависимые объекты, чтобы DROP TABLE CASCADE мог удалять всё, что ссылается
SET session_replication_role = replica;

-- Сначала удаляем дочерние таблицы, чтобы не было ошибок из-за FK
DROP TABLE IF EXISTS psd.main_materials_equipment CASCADE;
DROP TABLE IF EXISTS psd.main_mechanisms        CASCADE;
DROP TABLE IF EXISTS psd.work_volumes           CASCADE;

DROP TABLE IF EXISTS builders_specialists       CASCADE;
DROP TABLE IF EXISTS itr                         CASCADE;
DROP TABLE IF EXISTS aup                         CASCADE;

-- Таблицы-справочники (не зависят от psd.estimate_documentation напрямую, но у main_* есть ссылки)
DROP TABLE IF EXISTS materials_equipment        CASCADE;
DROP TABLE IF EXISTS mechanisms                 CASCADE;

-- Наконец, таблица сметной документации
DROP TABLE IF EXISTS psd.estimate_documentation CASCADE;

-- Если нужно полностью убрать схему psd, можно раскомментировать строку ниже:
-- DROP SCHEMA IF EXISTS psd CASCADE;

-- Включаем обратно проверку ограничений
SET session_replication_role = DEFAULT;

-- Таблица сметной документации
CREATE TABLE psd.estimate_documentation (
    estimate_id            SERIAL PRIMARY KEY,
    price_code_resource_codes VARCHAR(100),
    work_expense_name      VARCHAR(300),
    unit_of_measurement    VARCHAR(50),
    quantity               DECIMAL(10,2),
    unit_price_rub         DECIMAL(10,2),
    adjustment_coefficients TEXT,
    winter_increase_coefficients TEXT,
    total_base_cost_rub    DECIMAL(15,2),
    recalc_indices_standards TEXT,
    total_current_cost_rub DECIMAL(15,2)
);

-- Таблица спецификации материалов и оборудования
CREATE TABLE psd.main_materials_equipment (
    material_equipment_id  SERIAL PRIMARY KEY,
    estimate_id            INT       REFERENCES psd.estimate_documentation(estimate_id),
    name_technical_specification VARCHAR(300),
    type_brand_size        VARCHAR(100),
    unit_of_measurement    VARCHAR(50),
    quantity               DECIMAL(10,2),
    installation_location_method TEXT
);

-- Справочник материалов и оборудования
CREATE TABLE materials_equipment (
    material_equipment_id  SERIAL PRIMARY KEY,
    name                   VARCHAR(300),
    type_brand_size        VARCHAR(100),
    unit_of_measurement    VARCHAR(50),
    quantity               DECIMAL(10,2),
    supplier               VARCHAR(200),
    unit_cost_rub          DECIMAL(10,2),
    total_cost_rub         DECIMAL(15,2),
    storage_location       VARCHAR(200)
);

-- Таблица ведомости основных механизмов
CREATE TABLE psd.main_mechanisms (
    mechanism_id           SERIAL PRIMARY KEY,
    estimate_id            INT       REFERENCES psd.estimate_documentation(estimate_id),
    mechanism_name         VARCHAR(300),
    type_brand_load_capacity VARCHAR(100),
    quantity               INT,
    location               VARCHAR(200)
);

-- Справочник механизмов
CREATE TABLE mechanisms (
    mechanism_id           SERIAL PRIMARY KEY,
    mechanism_name         VARCHAR(300),
    type_brand_load_capacity VARCHAR(100),
    stock_quantity         INT,
    storage_location       VARCHAR(200),
    site_quantity          INT,
    stock_remaining        INT
);

-- Таблица ведомости объёмов работ
CREATE TABLE psd.work_volumes (
    work_id                SERIAL PRIMARY KEY,
    work_name              VARCHAR(300),
    unit_of_measurement    VARCHAR(50),
    quantity               DECIMAL(10,2),
    notes                  TEXT
);

-- Таблица строителей/специалистов
CREATE TABLE builders_specialists (
    specialist_id          SERIAL PRIMARY KEY,
    full_name              VARCHAR(300),
    position_specialty     VARCHAR(100),
    experience_years       INT,
    section                VARCHAR(100),
    salary                 DECIMAL(10,2)
);

-- Таблица инженерно-технических работников
CREATE TABLE itr (
    worker_id              SERIAL PRIMARY KEY,
    full_name              VARCHAR(300),
    position               VARCHAR(100),
    experience_years       INT,
    section                VARCHAR(100),
    salary                 DECIMAL(10,2)
);

-- Таблица административно-управленческого персонала
CREATE TABLE aup (
    staff_id               SERIAL PRIMARY KEY,
    full_name              VARCHAR(300),
    position               VARCHAR(100),
    experience_years       INT,
    section                VARCHAR(100),
    salary                 DECIMAL(10,2)
);

